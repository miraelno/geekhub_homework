# 7. Напишіть функцію, яка приймає 2 списки. Результатом має бути новий список, в якому знаходяться елементи першого списку, яких немає в другому. 
# Порядок елементів, що залишилися має відповідати порядку в першому (оригінальному) списку. 
# Реалізуйте обчислення за допомогою генератора.

def find_diff(list1, list2):

    result = [i for i in list1 if i not in list2]

    return result


list1 = [1, 2, 3, 4, 5]
list2 = [1, 2, 4, 10]

print(find_diff(list1, list2))
